use std::env::args;
use std::time::Instant;

fn main() {
    let now = Instant::now();
    let output = run(&args().nth(1).expect("Please provide an input"));
    let elapsed = now.elapsed();
    println!("_duration:{}", elapsed.as_secs_f64() * 1000.);
    println!("{}", output);
}

fn run(input: &str) -> usize {
    input
        .lines()
        .map(|line| {
            line.split_once(" | ")
                .unwrap()
                .1
                .split(' ')
                .map(|n| n.len())
                .fold(0, |acc, n| {
                    acc + if n == 2 || n == 3 || n == 4 || n == 7 {
                        1
                    } else {
                        0
                    }
                })
        })
        .sum()
}

